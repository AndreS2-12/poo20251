public class Ciclos {
    public static void main(String[] args) {
        // for (inicio; condicionde parada; incremento o decremento)
        for (int i = 1; i <= 5; i++) {
            System.out.println("Numero de iteración: " + i);
        }
        /*
         * Estructura del While
         * inicio;
         * While(condición){
         * ---- Porceso ----
         * incremento o drecremento
         * }
         */
        int j = 1;
        while (j <= 5) {
            System.out.println("Numero de iteración: " + j);
            j++;
        }
    }
}
